package pe.com.claro.eai.ebs.ws.venta.evaluacionbonosventa.mdb.bean;

public class TicklersConsultaType {

	private String tickNumber;
	private String tickCode;
	private String tickStatus;
	private String tickShdes;
	private String tickLdes;
	private String creationDate;
	private String closedDate;
		
	

	public String getClosedDate() {
		return closedDate;
	}

	public void setClosedDate(String closedDate) {
		this.closedDate = closedDate;
	}

	public String getTickNumber() {
		return tickNumber;
	}

	public void setTickNumber(String tickNumber) {
		this.tickNumber = tickNumber;
	}

	public String getTickCode() {
		return tickCode;
	}

	public void setTickCode(String tickCode) {
		this.tickCode = tickCode;
	}

	public String getTickStatus() {
		return tickStatus;
	}

	public void setTickStatus(String tickStatus) {
		this.tickStatus = tickStatus;
	}

	public String getTickShdes() {
		return tickShdes;
	}

	public void setTickShdes(String tickShdes) {
		this.tickShdes = tickShdes;
	}

	public String getTickLdes() {
		return tickLdes;
	}

	public void setTickLdes(String tickLdes) {
		this.tickLdes = tickLdes;
	}

	public String getCreationDate() {
		return creationDate;
	}

	public void setCreationDate(String creationDate) {
		this.creationDate = creationDate;
	}

}