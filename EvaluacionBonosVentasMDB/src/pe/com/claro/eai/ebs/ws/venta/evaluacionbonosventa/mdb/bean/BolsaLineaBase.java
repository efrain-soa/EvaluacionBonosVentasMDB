package pe.com.claro.eai.ebs.ws.venta.evaluacionbonosventa.mdb.bean;

public class BolsaLineaBase {

	private String bolsa;
	private String unidadesMedida;
	private Double unidadesAsignadas;
	private Double unidadesConsumidas;
	public String getBolsa() {
		return bolsa;
	}
	public void setBolsa(String bolsa) {
		this.bolsa = bolsa;
	}
	public String getUnidadesMedida() {
		return unidadesMedida;
	}
	public void setUnidadesMedida(String unidadesMedida) {
		this.unidadesMedida = unidadesMedida;
	}
	public Double getUnidadesAsignadas() {
		return unidadesAsignadas;
	}
	public void setUnidadesAsignadas(Double unidadesAsignadas) {
		this.unidadesAsignadas = unidadesAsignadas;
	}
	public Double getUnidadesConsumidas() {
		return unidadesConsumidas;
	}
	public void setUnidadesConsumidas(Double unidadesConsumidas) {
		this.unidadesConsumidas = unidadesConsumidas;
	}
	
	
}
